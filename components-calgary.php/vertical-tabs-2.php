const tablinks2 = Array.from(document.querySelectorAll('#vertical-tabs-2 .tablinks'));
const tabcontents2 = Array.from(document.querySelectorAll('#vertical-tabs-2 .tabcontent'));

// loop through all tab links
tablinks2.forEach(tablink => {
    // listen to which tab is clicked
    tablink.addEventListener('click', e => {
        // make sure all tablinks are removed with active class initially after clicked
        tablinks2.forEach(tab => {
            tab.classList.remove('active');
        })
        // add the class only to the clicked
        tablink.classList.add('active');

        // get the data attribute of the clicked tablink
        const quadname = tablink.getAttribute('data-quadrant')

        // loop through tab contents
        tabcontents2.forEach(tabcontent => {
            let content_id = tabcontent.getAttribute('id');

            if (content_id === quadname) {
                tabcontent.style.display = 'block'
            } else {
                tabcontent.style.display = 'none';
            }
        })

    })
})

document.getElementById('defaultOpen-2').click();
